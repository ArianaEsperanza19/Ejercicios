"""
3. Escribir un script que muestre los cuadrados (numero multiplicado por si mismo) de los primeros 60 numeros naturales. Resolverlo con for y while
"""

for i in range(1,60):
    print("El cuadrado de",i,"es",i*i,"\n-----------------------------------")

print("########################################################################")
j = 1
while j < 60:
    print("El cuadrado de",j,"es",j*j,"\n-----------------------------------")
    j += 1
