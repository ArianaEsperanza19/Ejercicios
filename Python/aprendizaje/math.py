import math
import random

print(math.pi)
n = 44
print(f"La raiz cuadrada de {n} es: ", math.sqrt(n))
print(f"El seno de {n} es: ", math.sin(n))
print(f"El coseno de {n} es: ", math.cosh(n) )
print(f"El logaritmo de {n} es: ", math.log(n))
print(f"El redondeo a la alta de {n} es: ", math.ceil(math.sqrt(n)))
print(f"El redondeo a la baja de {n} es: ", math.floor(math.sqrt(n)))
print(f"El numero aleatorio entre 15 y 67 es: ", random.randint(15, 67))
